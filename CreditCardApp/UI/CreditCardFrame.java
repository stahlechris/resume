    private void creditCardNumberTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_creditCardNumberTextFieldKeyTyped
        char c = evt.getKeyChar(); //get the key they pressed
        if (!(Character.isDigit(c) || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
        
    }//GEN-LAST:event_creditCardNumberTextFieldKeyTyped

