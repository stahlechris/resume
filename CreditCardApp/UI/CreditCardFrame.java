    private void creditCardNumberTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_creditCardNumberTextFieldFocusLost
        int counter = 1;
        String holder = creditCardNumberTextField.getText();
        Validator v = new Validator();
        //were gonna check if they entered 15 or 16 digits 
        if(!v.hasFifteenOrSixteenDigits(creditCardNumberTextField, "credit card number "))
        {
           creditCardNumberDisplayLabel.setText("Your CC needs the right amount of digits.");
        }
        String creditCardType = v.isValidCreditCardNum(creditCardNumberTextField, "credit card ");
        if(creditCardType != "" || creditCardType != null)
        {
            creditCardNumberDisplayLabel.setText("");
            creditCardNumberTextField.setForeground(new Color(102, 204, 0));
        }
        Border border = LineBorder.createBlackLineBorder();
        if(creditCardType == "Visa"){visaPictureLabel.setBorder(border);}
        if(creditCardType == "MasterCard"){masterCardPictureLabel.setBorder(border);}
        if(creditCardType == "American Express"){amexPictureLabel.setBorder(border);}
        if(creditCardType == "Discover"){discoverPictureLabel.setBorder(border);}
            
        
        //were gonna tell them they're type of cc 
    }//GEN-LAST:event_creditCardNumberTextFieldFocusLost

